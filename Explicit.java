public class Explicit {
    public static void main(String[] args) {
        double a = 50.50;
        System.out.println("Double representation " + a);

        float f = (float) a;
        System.out.println("Double representation " + f);

        long g = (long) a;
        System.out.println("Double representation " + g);

        int h = (int) a;
        System.out.println("Double representation " + h);

    }
}
